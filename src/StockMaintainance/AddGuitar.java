    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        int flag = 0;
        
        String model = modelTextField.getText();
        String builder = SelectBuilder.getSelectedItem().toString();
        String  type = typeComboBox.getSelectedItem().toString();
        String backWood = backWoodComboBox.getSelectedItem().toString();
        String topWood = topWoodCombox.getSelectedItem().toString();
        String color = colorTextField.getText();
        int year = yearPicker.getYear();
        
        if(year > 2021)
       {
               //JOptionPane.showMessageDialog(this, "Invalid Year");
                 flag = 1;
                 yearFlag.setVisible(true);    
                  
       }
      
        
        int serialNumber=0;
        try{
        serialNumber = Integer.parseInt(serialNumberTextField.getText());
        
        for(int i=0;i<guitar.size();i++)
        {
            if(guitar.get(i).getSerialNumber()==serialNumber)
            {
                JOptionPane.showMessageDialog(this, "Serial number already exists");
                flag = 1;
                
            }
        }
        }
        catch(Exception ex)
        {
            //JOptionPane.showMessageDialog(this, "Invalid Serial Number");
            flag = 1;
            serialFlag.setVisible(true);
        }
       
        double rentCost =0;
        try{
        rentCost = Double.parseDouble(rentTextField.getText());
        }
        catch (Exception ex)
        {
           // JOptionPane.showMessageDialog(this, "Invalid Rent");
            flag =1;
            rentPerDayFlag.setVisible(true);
        }
      /*  try
        {
            String color = colorTextField.getText();
            boolean isText = isString(color);
            if(isText == false)
            {
                flag =1;
                colorFlag.setVisible(true);
            }
        }
        catch(Exception ex)
        {
            
        }*/
        double length = 0;
        try
        {
            length = Double.parseDouble(lengthTextField.getText());
        }
        catch (Exception ex)
        {
          // JOptionPane.showMessageDialog(this, "Invalid Length"); 
            flag = 1;
            lengthFlag.setVisible(true);
        }
        double height = 0;
        try
        {
            height = Double.parseDouble(heightTextField.getText());
        }
        catch(Exception ex)
        {
          //  JOptionPane.showMessageDialog(this, "Invalid Height"); 
            flag =1;
            heightFlag.setVisible(true);
        }
        double width =0;
        try
        {
            
            width = Double.parseDouble(widthTextField.getText());
        }
        catch(Exception ex)
        {
           // JOptionPane.showMessageDialog(this, "Invalid Width"); 
            flag = 1;
            widthFlag.setVisible(true);
        }
        
    
        Guitar gt1 = new Guitar(model, Builder.valueOf(builder), StockMaintainance.Type.valueOf(type), Wood.valueOf(backWood), Wood.valueOf(topWood), serialNumber, year, rentCost, color, length, height, width, Status.AVAILABLE);
        if(flag ==0)
        {
            guitar.add(gt1);
            JOptionPane.showMessageDialog(this, "Guitar Added Successfully!!");
            resetFields(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please enter the required fields");
        }
               
        
    }//GEN-LAST:event_addButtonActionPerformed

