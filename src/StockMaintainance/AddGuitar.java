    Guitar searchGuitar(int serialNumber)
    {
        Guitar found = null;
        for(Guitar g: guitar)
        {
            if(g.getSerialNumber() == serialNumber)
            {
                found = g;
                break;
            }
        }
        return found;
    }
    
    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        int serialNumber = Integer.parseInt(serialNumberTextField.getText());
        Guitar g = searchGuitar(serialNumber);
        if(g ==null)
        {
            JOptionPane.showMessageDialog(this, "Sorry Guitar not found matching with this id");
        }
        else
        {
            
            JOptionPane.showMessageDialog(this, "Guitar found matching with this id");
            modelTextField.setText(g.getModel());
            SelectBuilder.getModel().setSelectedItem(g.getBuilder());
            typeComboBox.getModel().setSelectedItem(g.getType());
            backWoodComboBox.getModel().setSelectedItem(g.getBackWood());
            topWoodCombox.getModel().setSelectedItem(g.getTopWood());
            yearPicker.setYear(g.getYear());
            rentTextField.setText(g.getRentCost()+"");
            colorTextField.setText(g.getColor());
            lengthTextField.setText(g.getLength()+"");
            widthTextField.setText(g.getWidth()+"");
            heightTextField.setText(g.getHeight()+"");
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed

